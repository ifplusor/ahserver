/*
 * In Section 1.2. of [RFC7230], Syntax Notation:
 *
 * The following core rules are included by reference, as defined in [RFC5234], Appendix B.1
 *
 *   ALPHA          =  %x41-5A / %x61-7A
 *                          ; A-Z / a-z
 *
 *   CHAR           =  %x01-7F
 *                          ; any 7-bit US-ASCII character,
 *                          ;  excluding NUL
 *
 *   CR             =  %x0D
 *                          ; carriage return
 *
 *   CRLF           =  CR LF
 *                          ; Internet standard newline
 *
 *   CTL            =  %x00-1F / %x7F
 *                          ; controls
 *
 *   DIGIT          =  %x30-39
 *                          ; 0-9
 *
 *   DQUOTE         =  %x22
 *                          ; " (Double Quote)
 *
 *   HEXDIG         =  DIGIT / %x41-46 / 0x61-66
 *                          ; 0-9 / A-F / a-f
 *
 *   HTAB           =  %x09
 *                          ; horizontal tab
 *
 *   LF             =  %x0A
 *                          ; linefeed
 *
 *   OCTET          =  %x00-FF
 *                          ; 8 bits of data
 *
 *   SP             =  %x20
 *
 *   VCHAR          =  %x21-7E
 *                          ; visible (printing) characters
 *
 */

